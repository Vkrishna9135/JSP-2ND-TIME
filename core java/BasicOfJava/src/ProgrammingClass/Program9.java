public class Program9 {
	public static void main(String[] args) {
		
		char ch ='A';
		if(ch>='A'&&ch<='Z') {
			System.out.println("Char in upper case");
		}
		else if (ch>='a'&&ch<='z') {
			System.out.println("Char in lower case");
		}
		else {
			System.out.println("invalid charcter");
		}
		
		
	}

}
